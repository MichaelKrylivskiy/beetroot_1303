    to_sort = [15, 9, 8, 1, 4, 11, 7, 12, 13, 6, 5, 3, 16, 2, 10, 14]
    # Викликати функцію bubble_sort зі списком to_sort в якості аргументу і вивести результат.
    print(bubble_sort(to_sort))
